"""
AgentX Purple Agent Executor
============================
A2A AgentExecutor implementation for Purple Agent.
"""
from a2a.server.agent_execution import AgentExecutor, RequestContext
from a2a.server.events import EventQueue
from a2a.server.tasks import TaskUpdater
from a2a.types import (
    TaskState,
    UnsupportedOperationError,
    InvalidRequestError,
)
from a2a.utils.errors import ServerError
from a2a.utils import new_agent_text_message, new_task

from src.agents.agent import PurpleAgent


TERMINAL_STATES = {
    TaskState.completed,
    TaskState.canceled,
    TaskState.failed,
    TaskState.rejected
}


class PurpleExecutor(AgentExecutor):
    """
    A2A Executor for AgentX Purple Agent.
    """
    
    def __init__(self, mcp_endpoint: str | None = None):
        self.agents: dict[str, PurpleAgent] = {}
        self.mcp_endpoint = mcp_endpoint
    
    async def execute(
        self, 
        context: RequestContext, 
        event_queue: EventQueue
    ) -> None:
        """Execute agent logic for incoming request."""
        msg = context.message
        if not msg:
            raise ServerError(
                error=InvalidRequestError(message="Missing message in request")
            )
        
        task = context.current_task
        
        if task and task.status.state in TERMINAL_STATES:
            raise ServerError(
                error=InvalidRequestError(
                    message=f"Task {task.id} already processed"
                )
            )
        
        if not task:
            task = new_task(msg)
            await event_queue.enqueue_event(task)
        
        context_id = task.context_id
        
        agent = self.agents.get(context_id)
        if not agent:
            agent = PurpleAgent(mcp_endpoint=self.mcp_endpoint)
            self.agents[context_id] = agent
        
        updater = TaskUpdater(event_queue, task.id, context_id)
        
        await updater.start_work()
        try:
            await agent.run(msg, updater)
            if not updater._terminal_state_reached:
                await updater.complete()
        except Exception as e:
            print(f"âŒ Task failed: {e}")
            await updater.failed(
                new_agent_text_message(
                    f"Agent error: {e}",
                    context_id=context_id,
                    task_id=task.id
                )
            )
    
    async def cancel(
        self, 
        context: RequestContext, 
        event_queue: EventQueue
    ) -> None:
        """Cancel is not supported."""
        raise ServerError(error=UnsupportedOperationError())
